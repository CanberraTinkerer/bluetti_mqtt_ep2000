def bluetti_custom_crc(data: bytes) -> int:
    """
    Calculates the Bluetti V2 custom CRC-16.
    
    Args:
        data: The bytes to calculate the CRC over.
        
    Returns:
        The 16-bit CRC value as an integer.
    """
    # Initial state (matches i2 and b2 from b/c.java)
    i2 = 255 # low byte
    b2 = 255 # high byte
    
    for byte_val in data:
        # Match Java loop exactly: i3, i4, b3
        i3 = (i2 ^ byte_val) & 0xFF
        i4 = b2 ^ BLUETTI_F27A[i3]
        b3 = BLUETTI_F28B[i3]
        
        i2 = i4 & 0xFF
        b2 = b3 & 0xFF
        
    # Match Java return: ((b2 & 255) << 8) | (i2 & 255)
    return ((b2 << 8) | i2) & 0xFFFF